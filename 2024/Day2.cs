        private class Report
        {
            public IList<int> levels;

            public Report(IList<int> levels)
            {
                this.levels = levels;
            }

            public bool IsSafe()
            {
                if (this.levels.Count < 2)
                {
                    return false;
                }

                return this.IsSafe(this.levels, 0, false, true)
                    || this.IsSafe(this.levels, 1, false, false)
                    || this.IsSafe(this.levels, 0, true, true)
                    || this.IsSafe(this.levels, 1, true, false);
            }

            private bool IsSafe(IList<int> levels, int i, bool inc, bool canSkip)
            {
                if (i >= this.levels.Count - 1)
                {
                    return true;
                }

                if (this.IsSafe(this.levels, i, i + 1, inc)
                 && this.IsSafe(this.levels, i + 1, inc, canSkip))
                {
                    return true;
                }
                if (!canSkip)
                {
                    return false;
                }

                return this.IsSafe(this.levels, i, i + 2, inc)
                    && this.IsSafe(this.levels, i + 2, inc, false);
            }

            private bool IsSafe(IList<int> levels, int i1, int i2, bool inc)
            {
                if (i2 >= levels.Count)
                {
                    return true;
                }

                int diff = this.levels[i1] - this.levels[i2];
                if ((inc && diff > 0)
                 || (!inc && diff < 0))
                {
                    return false;
                }

                int diffAbs = Math.Abs(diff);
                if (diffAbs < 1 || diffAbs > 3)
                {
                    return false;
                }

                return true;
            }
        }

